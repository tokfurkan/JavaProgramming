package day16_ForLoopsStringPractice.day17_While_DoWhile;

public class WhileLoopIntro {

    public static void main(String[] args) {

        if (true){
            System.out.println("Wooden Spoon"); //1 time
        }
        while (true){ // repeated if
            System.out.println("Wooden Spoon"); // it will be executed until it becomes false
        }



    }
}
