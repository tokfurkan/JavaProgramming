package day09_IfStatements_2;

public class MaximumNumber {

    public static void main(String[] args) {

        int num1 = 14,
                num2 = 18;

        if (num1 > num2) {
            System.out.println(num1 + " is the maximum number");
        } else {
            System.out.println(num2 + " is the maximum number");
        }




    }
}
