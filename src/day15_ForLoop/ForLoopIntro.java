package day15_ForLoop;

public class ForLoopIntro {

    public static void main(String[] args) {

        // we use for loop when we know exact number of repeating
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        System.out.println("--------------------------");

        //          1       2       4
        for (int i = 1; i <= 10; i ++){
            System.out.println("Hello World"); //3
        }


    }
}
